## Complex Numbers in Cartesian Form

```math
\begin{align*}
i = \sqrt{-1}
\end{align*}
```

``i`` behaves like real numbers in all manipulations involving addition and multiplication. The only new addition is that it's square is equal to -1. The general complex number then becomes

```math
\begin{align*}
z = x + iy
\end{align*}
```

If the complex numbers ``z_1 = z_2``, then the parts of ``z_1`` and ``z_2`` must be exactly equal.

```math
\begin{align*}
Re \ z &= x = \frac{z + z^*}{2} \\
Im \ z &= y = \frac{z - z^*}{2i} \\
\end{align*}
```

Complex conjugation (``z^*``) can be viewed as the process of replacing ``i`` by ``-i`` within the complex number

"""

