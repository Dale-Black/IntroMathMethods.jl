# ╔═╡ 10907e21-5ffe-4ef7-9501-0f1c65debe9d
md"""
!!! warning "By Hand"
	  - Let's consider a polynomial ``p(x)`` with real coefficients and suppose that ``a + bi`` (where ``a`` and ``b`` are real numbers, and ``b ≠ 0``) is a root of this polynomial. This means that when we substitute ``x = a + bi`` into ``p(x)``, we get zero:

    ```math
    \begin{align*}
    p(a + bi) = 0
    \end{align*}
    ```

	  - Now, take the complex conjugate of both sides of this equation. The left-hand side becomes the polynomial evaluated at the complex conjugate of the root, and the right-hand side remains zero (since the complex conjugate of zero is zero):

    ```math
    \begin{align*}
    p(a - bi) = 0
    \end{align*}
    ```

	- Therefore, ``a - bi`` is also a root of the polynomial. This completes the proof.

	So, the roots of a polynomial with real coefficients will always be either real or pairs of complex conjugates.

"""

