# ╔═╡ a7f51817-04c4-4f93-b5e0-482bd0b359d7
md"""
!!! warning "By Hand"
	Properties of Complex Numbers
	- The properties of complex numbers can be proven algebraically. Let's consider a complex number ``z = a + bi``, where ``a`` and ``b`` are real numbers, and ``i`` is the imaginary unit. The modulus (or absolute value) of ``z`` is defined as ``|z| = sqrt(a^2 + b^2)``.

	1 ``|Re \ z| ≤ |z|``:
	
	- The real part of ``z`` is ``a``, and its absolute value is ``|a|``. We have:
	
	```math
	\begin{align*}
	|a| &= \sqrt{a^2} \leq \sqrt{a^2 + b^2} = |z| \\
	\therefore |Re \ z| &≤ |z|
	\end{align*}
	```
	
	2 ``|Im \ z| ≤ |z|``:
	
	- The imaginary part of ``z`` is ``b``, and its absolute value is ``|b|``. We have:
	
	```math
	\begin{align*}
	|b| &= \sqrt{b^2} \leq \sqrt{a^2 + b^2} = |z| \\
	\therefore |Im \ z| &≤ |z|
	\end{align*}
	```
	
	3 ``|z_1 + z_2|^2 = |z_1|^2 + |z_2|^2 + 2 Re(z_1 z_2^*)``:
	
	- Let ``z_1 = a + bi`` and ``z_2 = c + di``

	```math
	\begin{align*}
	z_1 + z_2 &= (a+c) + (b+d)i \\
	z_1 z_2^* &= (a+bi)(c-di) = (ac+bd) + (bc-ad)i \\
	|z_1 + z_2|^2 &= |(a+c) + (b+d)i|^2 \\
	&= (a+c)^2 + (b+d)^2 \\
	&= a^2 + 2ac + c^2 + b^2 + 2bd + d^2 \\
	&= |z_1|^2 + |z_2|^2 + 2(ac + bd) \\
	&= |z_1|^2 + |z_2|^2 + 2 Re(z_1 z_2^*)
	\end{align*}
	```
	
	4 ``|z_1 + z_2| ≤ |z_1| + |z_2|``:

	```math
	\begin{align*}
	|z_1 + z_2|^2 &≤ |z_1|^2 + |z_2|^2 + 2|z_1||z_2| \\
	|z_1 + z_2| &≤ \sqrt{|z_1|^2 + |z_2|^2 + 2|z_1||z_2|} = |z_1| + |z_2|
	\end{align*}
	```
	
	5 ``|z_1 z_2| = |z_1||z_2|``:
	
	- Let ``z_1 = a + bi`` and ``z_2 = c + di``. Then ``z_1 z_2 = (ac - bd) + (ad + bc)i``. We have:

	```math
	\begin{align*}
	|z_1 z_2| &= \sqrt{(ac - bd)^2 + (ad + bc)^2} \\
	&= \sqrt{a^2 c^2 - 2abcd + b^2 d^2 + a^2 d^2 + 2abcd + b^2 c^2} \\
	&= \sqrt{a^2 (c^2 + d^2) + b^2 (c^2 + d^2)} \\
	&= \sqrt{(a^2 + b^2)(c^2 + d^2)} \\
	&= |z_1||z_2| \\
	\therefore |z_1 z_2| &= |z_1||z_2|
	\end{align*}
	```

"""

