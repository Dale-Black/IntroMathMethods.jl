md"""
!!! warning "By Hand"

	1. ``\sin^2(x) + \cos^2(x) = 1``
	
	```math
	\begin{align*}
	\sin(x) &= \frac{e^{ix} - e^{-ix}}{2i} \\
	\cos(x) &= \frac{e^{ix} + e^{-ix}}{2} \\
	\sin^2(x) + \cos^2(x) &= \left(\frac{e^{ix} - e^{-ix}}{2i}\right)^2 + \left(\frac{e^{ix} + e^{-ix}}{2}\right)^2 \\
	&= \frac{1}{-4} + \frac{1}{4} = 0 + 1 = 1
	\end{align*}
	```
	
	2. ``\sin(2x) = 2 \sin(x) \cos(x)``
	
	```math
	\begin{align*}
	\sin(2x) &= \frac{e^{i2x} - e^{-i2x}}{2i} \\
	2 \sin(x) \cos(x) &= 2 \left(\frac{e^{ix} - e^{-ix}}{2i}\right) \left(\frac{e^{ix} + e^{-ix}}{2}\right) \\
	&= \frac{e^{i2x} - e^{-i2x}}{2i}
	\end{align*}
	```
	
	3. ``\cos(2x) = \cos^2(x) - \sin^2(x)``
	
	```math
	\begin{align*}
	\cos(2x) &= \frac{e^{i2x} + e^{-i2x}}{2} \\
	\cos^2(x) - \sin^2(x) &= \left(\frac{e^{ix} + e^{-ix}}{2}\right)^2 - \left(\frac{e^{ix} - e^{-ix}}{2i}\right)^2 \\
	&= \frac{e^{i2x} + e^{-i2x}}{2}
	\end{align*}
	```

